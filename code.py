# Programme simple : calcul de la somme de deux nombres

# Demande à l'utilisateur d'entrer deux nombres
a = float(input("Entrez le premier nombre : "))
b = float(input("Entrez le deuxième nombre : "))

# Calcule la somme
somme = a + b

# Affiche le résultat
print("La somme de", a, "et", b, "est :", somme)
